package ai.dev.kit.eval.utils

/**
 * A base abstract class for running evaluation tests without external logging or tracking.
 *
 * This class reuses the mechanics of [BaseEvaluationTest] but intentionally disables any experiment naming and
 * persistence to external systems.
 * Internally, it sets:
 * - experimentName = null
 * - runNamePrefix = null
 * - evaluationClient = null
 *
 * Use this when you want ful local/in-memory evaluation without emitting traces or writing
 * results to providers.
 *
 * Type parameters:
 * @param AIInputT The type of input provided to the AI system under test.
 * @param GroundTruthT The type of ground-truth data used for evaluation.
 * @param AIOutputT The type of output generated by the AI system.
 * @param EvalResultT The type of evaluation result representing scores or metrics assigned for a test.
 *
 * @param numberOfRuns The number of test runs to execute during the evaluation process. Default is 1.
 */
abstract class ConsoleEvaluationTest<AIInputT : AIInput, GroundTruthT : GroundTruth, AIOutputT : AIOutput, EvalResultT : EvalResult>(
    numberOfRuns: Int = 1,
) :
    BaseEvaluationTest<AIInputT, GroundTruthT, AIOutputT, EvalResultT>(
        experimentName = null,
        runNamePrefix = null,
        numberOfRuns,
        listOf(),
        null
    )