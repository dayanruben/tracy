    jvmToolchain(17)

    jvm {
        compilerOptions.jvmTarget = JVM_17
    }

    sourceSets {
        val commonMain by getting {
            kotlin.srcDir(layout.buildDirectory.dir("generated/commonMain"))
        }

        jvmMain {
            dependencies {
                implementation(libs.okhttp)
                implementation(libs.ktor.client)
