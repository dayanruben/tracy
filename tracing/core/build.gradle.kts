val generatedBuildConfigDir = layout.buildDirectory.dir("generated/commonMain")
val version = providers.gradleProperty("VERSION").orNull
    ?: error("Missing VERSION property")

val generateBuildConfig by tasks.registering {
    val outputDir = generatedBuildConfigDir.get()
    outputs.dir(generatedBuildConfigDir)

    doLast {
        val file = outputDir.file("ai/jetbrains/tracy/config/BuildConfig.kt").asFile
        file.parentFile.mkdirs()
        file.writeText(
            """
            // Generated by Gradle task 'generateBuildConfig'.
            // Do not edit manually! Changes will be overwritten.
            
            package ai.jetbrains.tracy.config
            
            object BuildConfig {
                const val VERSION: String = "$version"
            }
            """.trimIndent()
        )
    }
}

tasks.named("dokkaGeneratePublicationHtml") {
    dependsOn(generateBuildConfig)
}
tasks.named("dokkaGeneratePublicationJavadoc") {
    dependsOn(generateBuildConfig)
}
tasks.named("dokkaGenerateModuleHtml") {
    dependsOn(generateBuildConfig)
}
tasks.named("dokkaGenerateModuleJavadoc") {
    dependsOn(generateBuildConfig)
}

tasks.named("jvmSourcesJar") {
    dependsOn(generateBuildConfig)
}

tasks.withType<org.jetbrains.kotlin.gradle.tasks.KotlinCompile>().configureEach {
    dependsOn(generateBuildConfig)
    compilerOptions {
        freeCompilerArgs.addAll("-java-parameters", "-Xexpect-actual-classes")
    }
}


publishing {
    publications.withType<MavenPublication>().configureEach {
        artifactId = "tracy-$artifactId"
        pom {
            name.set(artifactId)
            description.set("Core tracing module of the Tracy library.")
        }
    }
}

