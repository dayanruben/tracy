package ai.dev.kit.eval.utils

import ai.dev.kit.tracing.AI_DEVELOPMENT_KIT_TRACER
import ai.dev.kit.tracing.TracingManager
import ai.dev.kit.tracing.fluent.FluentSpanAttributes
import ai.dev.kit.tracing.fluent.TracingSessionProvider.currentSessionId
import ai.dev.kit.tracing.fluent.dataclasses.RunStatus
import ai.dev.kit.tracing.fluent.withSessionIdBlocking
import io.opentelemetry.api.GlobalOpenTelemetry
import io.opentelemetry.api.trace.Span
import io.opentelemetry.api.trace.Tracer
import io.opentelemetry.extension.kotlin.asContextElement
import kotlinx.coroutines.runBlocking
import kotlinx.coroutines.withContext
import mu.KotlinLogging
import org.junit.jupiter.api.*

/**
 * A base abstract class for conducting evaluation tests on AI functionality.
 * The class manages multiple test runs within an experiment and provides mechanisms for evaluating
 * AI outputs against ground-truth data while logging the results into [EvaluationClient].
 *
 * @param AIInputT The type of input provided to the AI system under test.
 * @param GroundTruthT The type of ground-truth data used for evaluation.
 * @param AIOutputT The type of output generated by the AI system.
 * @param EvalResultT The type of evaluation result representing scores or metrics assigned for a test.
 * @param experimentName The name of the experiment in an observability platform where the test results will be logged.
 * @param runNamePrefix A prefix used for naming the runs within the experiment.
 * @param numberOfRuns The number of test runs to execute during the evaluation process.
 * @param tags A list of tags associated with each run, used for metadata. The size of this list must match `numberOfRuns`.
 * @param loggingClient An implementation of the [EvaluationClient] interface that handles logging of evaluation workflows.
 *                      Current implementations include clients: `LangfuseEvaluationClient`.
 */
@TestInstance(TestInstance.Lifecycle.PER_CLASS)
abstract class BaseEvaluationTest<
        AIInputT : AIInput,
        GroundTruthT : GroundTruth,
        AIOutputT : AIOutput,
        EvalResultT : EvalResult
        >(
    private val experimentName: String,
    private val runNamePrefix: String,
    private val numberOfRuns: Int,
    private val tags: List<RunTag>,
    private val loggingClient: EvaluationClient,
) {
    init {
        require(tags.isEmpty() || tags.size == numberOfRuns) { "The number of tags must match the number of runs" }
    }

    private val logger = KotlinLogging.logger {}
    private lateinit var experimentId: String
    private val runResults = mutableListOf<RunResults<AIInputT, GroundTruthT, AIOutputT, EvalResultT>>()

    @BeforeAll
    fun beforeAll() = runBlocking {
        logger.info { "🔄 Setting up before all tests" }

        experimentId = loggingClient.getOrCreateExperiment(experimentName)
            ?: error("Experiment $experimentName not found and could not be created")
        (1..numberOfRuns).map { runNum ->
            val runId = loggingClient.createRun(
                experimentId, runNamePrefix + if (numberOfRuns > 1) "/subrun-$runNum" else ""
            )
            runResults.add(RunResults(mutableListOf(), runId, RunStatus.FINISHED))
        }
        return@runBlocking
    }

    @AfterAll
    fun afterAll() = runBlocking {
        logger.info { "📊 Logging evaluation results" }
        TracingManager.flushTraces()
        TracingManager.shutdownTracing()
        runResults.forEachIndexed { runIndex, runResult ->
            val (testResults, runId, runStatus) = runResult
            try {
                if (runIndex < tags.size) loggingClient.applyTag(runId, tags[runIndex])
                loggingClient.uploadResults(runId, testResults)
                logAverageScore(runId, testResults.map { it.evalResult })
            } catch (_: Exception) {
                runResults[runIndex].finalStatus = RunStatus.FAILED
            } finally {
                loggingClient.changeRunStatus(runId, runStatus)
            }
        }
    }

    @TestFactory
    fun Runs() = runResults.mapIndexed { runNum, runResult ->
        DynamicContainer.dynamicContainer(
            "Run ${if (runResults.size > 1) runNum + 1 else ""}",
            testCases.mapIndexed { dataPointIndex, testCase ->
                DynamicTest.dynamicTest(testCase.name) {
                    withSessionIdBlocking(runResult.runId) {
                        val (dataPointSpan, dataPointScope) = createDataPointSpan(
                            dataPointIndex,
                            GlobalOpenTelemetry.getTracer(AI_DEVELOPMENT_KIT_TRACER),
                            testCase
                        )

                        val traceId = dataPointSpan.spanContext.traceId
                        dataPointSpan.makeCurrent()

                        try {
                            val output = withContext(dataPointSpan.asContextElement()) {
                                generator.generate(testCase.input)
                            }
                            executeSingleTest(testCase.name, testCase, runNum, output, traceId)
                        } finally {
                            dataPointSpan.end()
                            dataPointScope.close()
                        }
                    }
                }
            }
        )
    }

    private fun createDataPointSpan(
        dataPointIndex: Int, tracer: Tracer, testCase: TestCase<AIInputT, GroundTruthT>
    ): Pair<Span, io.opentelemetry.context.Scope> {
        val tracedRunName = testCase.name.ifBlank { "Data Point ${dataPointIndex + 1}" }
        val dataPointSpan = tracer.spanBuilder(tracedRunName).setNoParent().let {
            it.setAttribute(
                FluentSpanAttributes.SPAN_INPUTS.key, testCase.input.toString()
            )
            it.startSpan()
        }

        val dataPointScope = dataPointSpan.makeCurrent()
        return dataPointSpan to dataPointScope
    }

    private fun executeSingleTest(
        testCaseName: String,
        testCase: TestCase<AIInputT, GroundTruthT>,
        runNum: Int,
        output: AIOutputT,
        traceId: String
    ) {
        val result = try {
            evaluator.evaluate(testCase.groundTruth, output)
        } catch (e: Throwable) {
            val message = "❌ Test Failed: $testCaseName | Case: $testCase | Reason: Evaluator has thrown ${e.message}"
            logTest(message)
            fail(message)
        }
        runResults[runNum].testResults.add(
            TestResult(
                testCase,
                output,
                result,
                traceId
            )
        )

        if (result.hasJunitTestSucceeded) {
            logTest(
                message = "✅ Test Passed: $testCaseName | Case: $testCase | Result: $result"
            )
        } else {
            val message = "❌ Test failed: $testCaseName | Case: $testCase | Result: $result"
            logTest(message)
            fail(message)
        }
    }

    private fun logTest(message: String) {
        logger.info { message }
        currentSessionId?.let {
            val resultsLink = loggingClient.getResultsLink(experimentId, it)
            logger.info { "🔗 View results at $resultsLink" }
        }
    }

    private suspend fun logAverageScore(runId: String, evalResults: List<EvalResultT>) {
        val aggregateScores = evaluator.aggregateResults(evalResults)
        for (agg in aggregateScores) {
            loggingClient.logMetric(
                runId, agg.scoreName, agg.score
            )
        }
        if (aggregateScores.isNotEmpty()) {
            logger.info { "📈 Results logged to ${loggingClient.clientName} for Run ID: $runId" }
        }
    }

    abstract val testCases: List<TestCase<AIInputT, GroundTruthT>>
    abstract val generator: Generator<AIInputT, AIOutputT>
    abstract val evaluator: Evaluator<GroundTruthT, AIOutputT, EvalResultT>
}
